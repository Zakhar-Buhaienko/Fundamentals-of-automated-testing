import { abs, floor, ceil } from "./sample";

describe("abs", () => {
  test("Повертає саме число, якщо воно додатнє", () => {
    expect(abs(5)).toBe(5);
  });

  test("Повертає протилежне число, якщо воно від’ємне", () => {
    expect(abs(-7)).toBe(7);
  });

  test("Повертає 0 для 0", () => {
    expect(abs(0)).toBe(0);
  });

  test("Повертає абсолютне значення з десятковим дробом", () => {
    expect(abs(-3.14)).toBe(3.14);
  });

  test("Повертає правильне значення для дуже великого від’ємного числа", () => {
    expect(abs(-1000000)).toBe(1000000);
  });
});

describe("floor", () => {
  test("Округлює десяткове число вниз", () => {
    expect(floor(3.9)).toBe(3);
  });

  test("Округлює додатне ціле число без змін", () => {
    expect(floor(4)).toBe(4);
  });

  test("Округлює від’ємне десяткове число вниз до меншого цілого", () => {
    expect(floor(-1.1)).toBe(-2);
  });

  test("Округлює нуль", () => {
    expect(floor(0)).toBe(0);
  });

  test("Округлює велике число", () => {
    expect(floor(123456.789)).toBe(123456);
  });
});

describe("ceil", () => {
  test("Округлює десяткове число вгору", () => {
    expect(ceil(2.1)).toBe(3);
  });

  test("Округлює ціле число без змін", () => {
    expect(ceil(5)).toBe(5);
  });

  test("Округлює від’ємне десяткове число вгору до меншого за модулем", () => {
    expect(ceil(-3.9)).toBe(-3);
  });

  test("Округлює 0", () => {
    expect(ceil(0)).toBe(0);
  });

  test("Округлює велике дробове число", () => {
    expect(ceil(9999.0001)).toBe(10000);
  });
});
